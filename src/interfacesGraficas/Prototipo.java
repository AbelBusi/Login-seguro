    private void labelentrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelentrarMouseClicked
        // TODO add your handling code here:
        
        sqlUsuarios modSql = new sqlUsuarios();
        usuarios mod = new usuarios();
        
        String pass = new String(txtpassword.getPassword());
        
        if(!txtusuario.getText().equals("") && !pass.equals("")){
            
            String nuevoPass = hash.sha1(pass);
            
            mod.setUsuario(txtusuario.getText());
            mod.setPassword(nuevoPass);
            
            if (modSql.Login(mod)){
                
                Cuenta_cliente_form llamadaProductos = new  Cuenta_cliente_form();
                llamadaProductos.setVisible(true);
                this.setVisible(false);
                
            }else{
            JOptionPane.showMessageDialog(null, "Datos incorrectos");
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Debe ingresar sus datos");

        }
        
        
        
        
        //String usuario =txtusuario.getText();
        //String password = txtpassword.getText();
        
        
        panelInciarSesion.setBackground(Color.BLACK);
        labelentrar.setForeground(Color.WHITE);
    }//GEN-LAST:event_labelentrarMouseClicked

